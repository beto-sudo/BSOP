  FROM to_insert_personas tip
  RETURNING id, rfc
),
all_personas AS (
  SELECT excel_rfc AS rfc, persona_id FROM personas_dilesa_match
  UNION ALL
  SELECT rfc, id FROM inserted_personas
),
to_insert_proveedores AS (
  SELECT e.codigo, ap.persona_id, e.tasa_iva
  FROM excel e
  JOIN all_personas ap ON ap.rfc = e.rfc
  WHERE NOT EXISTS (
    SELECT 1 FROM erp.proveedores pr
    WHERE pr.persona_id = ap.persona_id
      AND pr.empresa_id = (SELECT id FROM dilesa)
      AND pr.deleted_at IS NULL
  )
),
inserted_proveedores AS (
  INSERT INTO erp.proveedores (empresa_id, persona_id, codigo, tasa_iva, activo)
  SELECT (SELECT id FROM dilesa), tip.persona_id, tip.codigo, tip.tasa_iva, true
  FROM to_insert_proveedores tip
  RETURNING id
)
SELECT
  (SELECT COUNT(*) FROM excel) AS rfcs_excel,
  (SELECT COUNT(*) FROM personas_dilesa_match) AS ya_persona_dilesa,
  (SELECT COUNT(*) FROM inserted_personas) AS personas_creadas,
  (SELECT COUNT(*) FROM inserted_proveedores) AS proveedores_creados;

COMMIT;
